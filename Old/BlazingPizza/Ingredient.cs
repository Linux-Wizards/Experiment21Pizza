/// <summary>
/// Class <c>Ingredient</c> represents an ingredient that food can be composed of.
/// </summary>
public class Ingredient
{
    /// <summary>
    /// Variable <c>Name</c> represents the name of the ingredient.
    /// </summary>
    public required String Name { get; set; }
    /// <summary>
    /// Variable <c>Price</c> stores the price of the ingredient.
    /// </summary>
    public double Price { get; set; }
}
